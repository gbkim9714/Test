#include <string>
#include <vector>

using namespace std;

/*

JadenCase란 모든 단어의 첫 문자가 대문자이고, 그 외의 알파벳은 소문자인 문자열입니다. 
단, 첫 문자가 알파벳이 아닐 때에는 이어지는 알파벳은 소문자로 쓰면 됩니다. (첫 번째 입출력 예 참고)
문자열 s가 주어졌을 때, s를 JadenCase로 바꾼 문자열을 리턴하는 함수, solution을 완성해주세요.

제한 조건
s는 길이 1 이상 200 이하인 문자열입니다.
s는 알파벳과 숫자, 공백문자(" ")로 이루어져 있습니다.
숫자는 단어의 첫 문자로만 나옵니다.
숫자로만 이루어진 단어는 없습니다.
공백문자가 연속해서 나올 수 있습니다.

*/

/* NotePad
   
   입력받은 String에서 띄어쓰기인지 판단해서 값을 넣어야 됨
   대문자 : 65 ~ 90, 소문자 97 ~ 122

   시작문자가 소문자일 경우 대문자로 변경해야 하고, 뒤쪽 문자가 대문자일 경우 소문자로 변경해야 한다.
*/

string solution(string s) {

    bool bUpper{ true };     // 가장 앞 단어는 무조건 대문자

    for (auto& iter : s)
    {
        int iCurAscii = static_cast<int>(iter);

        if (bUpper)
        {
            if (97 <= iCurAscii && 122 >= iCurAscii)
            {
                iCurAscii -= 32;
                iter = static_cast<char>(iCurAscii);
            }

            bUpper = false;
        }
        else
        {
            if (65 <= iCurAscii && 90 >= iCurAscii)
            {
                iCurAscii += 32;
                iter = static_cast<char>(iCurAscii);
            }
        }

        if (iter == ' ')
        {
            bUpper = true;
        }
    }

    string answer{ s };

    return answer;
}

int main()
{
    solution("3people unFollowed me");

    return 0;
}